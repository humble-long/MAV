def main():
    if len(ORGANISMS) != 23:
        print(f"⚠ 期望 23 个，实际 {len(ORGANISMS)} 个")

    print(f"准备写入 {len(ORGANISMS)} 个 Organism 节点的属性...")
    driver = GraphDatabase.driver(URI, auth=(USER, PASSWORD))

    updated = 0
    skipped = 0
    with driver.session() as sess:
        for name, props in ORGANISMS.items():
            # 检查节点存在
            res = sess.run(
                "MATCH (n:Organism {name:$name}) RETURN count(n) AS c",
                name=name,
            )
            cnt = res.single()["c"]
            if cnt == 0:
                print(f"  ⚠ Organism 节点不存在: {name!r}，跳过")
                skipped += 1
                continue

            # 写属性 - 用 SET 一次性覆盖
            payload = dict(props)
            payload["last_enriched_at"] = datetime.now().strftime("%Y-%m-%d")
            payload["enriched_by"] = "P0-1 script"

            sess.run(
                """
                MATCH (n:Organism {name:$name})
                SET n += $props
                """,
                name=name,
                props=payload,
            )
            updated += 1
            print(f"  ✅ {name} ({props.get('english_name', '?')})")

    driver.close()

    print(f"\n=== 完成 ===")
    print(f"  Updated: {updated}")
